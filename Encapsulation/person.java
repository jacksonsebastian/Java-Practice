public class person {
    //Access modifier
    private String name;
    private int rollno;

    //Getter


    public int getRollno(){
        return rollno;
    }

    public void setRollno(int rollno) {
        this.rollno = rollno;
    }

    public String getname() {
        return name;
    }

    public void setname(String newName) {
        this.name = newName;
    }
}
